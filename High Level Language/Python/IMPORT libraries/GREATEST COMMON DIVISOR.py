import math
#greatest common divisor
# GCD of two positive numbers
print(math.gcd(48, 18))  # Output: 6

# GCD of a positive number and a negative number
print(math.gcd(48, -18))  # Output: 6

# GCD of two negative numbers
print(math.gcd(-48, -18))  # Output: 6

# GCD where one number is zero
print(math.gcd(48, 0))  # Output: 48

# GCD of zero and zero
print(math.gcd(0, 0))  # Output: 0
